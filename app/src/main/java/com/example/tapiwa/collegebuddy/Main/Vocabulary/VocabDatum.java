public class VocabDatum {

    private String word;
    private String meaning;

    public VocabDatum() {

    }

    public VocabDatum(String word, String meaning) {
        this.word = word;
        this.meaning = meaning;
    }


    public String getWord() {
        return word;
    }

    public String getMeaning() {
        return meaning;
    }



}
